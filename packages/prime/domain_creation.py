class DomainCreationError(Exception):
    """Base exception for domain creation operations."""


class DuplicateDomainError(DomainCreationError):
    """Raised when a domain with the same name or ID already exists."""


class BlueprintValidationError(DomainCreationError):
    """Raised when a blueprint fails validation."""


class BlueprintNotFoundError(DomainCreationError):
    """Raised when a requested blueprint does not exist."""


class DomainNotApprovedError(DomainCreationError):
    """Raised when trying to register a domain whose proposal is not approved."""


