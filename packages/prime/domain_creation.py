class AgentBlueprint(BaseModel):
    """Blueprint for an agent to be created within a domain."""

    agent_id: str
    name: str
    role: AgentRole = AgentRole.SPECIALIST
    goal: str = ""
    backstory: str = ""
    capabilities: list[str] = []
    tools: list[str] = []


class SkillBlueprint(BaseModel):
    """Blueprint for a skill (new or reused) within a domain."""

    skill_id: str
    name: str
    description: str = ""
    version: str = "0.1.0"
    is_reused: bool = False  # True if the skill already exists in the registry
    source_domain: str | None = None  # Domain the reused skill came from


class WorkflowBlueprint(BaseModel):
    """Blueprint for a starter workflow/crew within a domain."""

    workflow_id: str
    name: str
    workflow_type: WorkflowType = WorkflowType.SEQUENTIAL
    description: str = ""
    agent_ids: list[str] = []  # Agents involved in this workflow
    steps: list[str] = []  # Ordered steps or phases


class DomainConfig(BaseModel):
    """Domain-specific configuration and constraints."""

    max_agents: int = 10
    max_concurrent_tasks: int = 5
    requires_human_approval: bool = True
    data_retention_days: int = 90
    priority_level: str = "normal"  # "low", "normal", "high", "critical"
    custom_settings: dict[str, object] = {}


class DomainBlueprint(BaseModel):
    """Complete blueprint for a new domain.

    Generated from a natural language description, validated for
    completeness, and submitted as a Proposal for approval.
    """

    id: UUID = Field(default_factory=uuid4)
    domain_name: str
    domain_id: str = ""  # Slug-style identifier (auto-generated)
    description: str = ""
    source_description: str = ""  # Original NL description
    agents: list[AgentBlueprint] = []
    skills: list[SkillBlueprint] = []
    workflows: list[WorkflowBlueprint] = []
    config: DomainConfig = Field(default_factory=DomainConfig)
    creation_mode: CreationMode = CreationMode.HUMAN_GUIDED
    status: DomainStatus = DomainStatus.DRAFT
    proposal_id: UUID | None = None
    created_by: str = "prime"
    created_at: datetime = Field(default_factory=lambda: datetime.now(UTC))
    validation_errors: list[str] = []
    validation_warnings: list[str] = []


class DomainCreationResult(BaseModel):
    """Result of a domain creation attempt."""

    blueprint: DomainBlueprint
    proposal_id: UUID | None = None
    registered: bool = False
    domain: DomainDescriptor | None = None
    message: str = ""


