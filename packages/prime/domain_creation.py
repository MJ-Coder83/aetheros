class BlueprintStore:
    """In-memory store for domain blueprints."""

    def __init__(self) -> None:
        self._blueprints: dict[UUID, DomainBlueprint] = {}

    def add(self, blueprint: DomainBlueprint) -> None:
        self._blueprints[blueprint.id] = blueprint

    def get(self, blueprint_id: UUID) -> DomainBlueprint | None:
        return self._blueprints.get(blueprint_id)

    def get_by_domain_id(self, domain_id: str) -> DomainBlueprint | None:
        for bp in self._blueprints.values():
            if bp.domain_id == domain_id:
                return bp
        return None

    def list_all(self) -> list[DomainBlueprint]:
        return list(self._blueprints.values())

    def update(self, blueprint: DomainBlueprint) -> None:
        if blueprint.id not in self._blueprints:
            raise BlueprintNotFoundError(
                f"Blueprint {blueprint.id} not found"
            )
        self._blueprints[blueprint.id] = blueprint

    def remove(self, blueprint_id: UUID) -> None:
        with contextlib.suppress(KeyError):
            del self._blueprints[blueprint_id]


