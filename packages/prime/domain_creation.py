    def generate(
        self,
        description: str,
        domain_name: str | None = None,
        creation_mode: CreationMode = CreationMode.HUMAN_GUIDED,
        existing_skills: list[SkillDescriptor] | None = None,
    ) -> DomainBlueprint:
        """Generate a domain blueprint from a natural language description.

        Args:
            description: Natural language description of the desired domain.
            domain_name: Optional explicit domain name; auto-generated if not provided.
            creation_mode: How the domain was created.
            existing_skills: Skills already in the registry for potential reuse.

        Returns:
            A DomainBlueprint ready for validation and proposal submission.
        """
        desc_lower = description.lower()
        archetype_key = self._detect_archetype(desc_lower)

        # Generate domain name and ID
        name = domain_name or self._generate_name(description, archetype_key)
        domain_id = self._generate_id(name)

        # Generate domain description
        domain_desc = self._generate_description(description, archetype_key)

        # Generate agents
        agents = self._generate_agents(desc_lower, archetype_key, domain_id)

        # Generate skills
        skills = self._generate_skills(
            desc_lower, archetype_key, domain_id, existing_skills or [],
        )

        # Generate workflows
        workflows = self._generate_workflows(
            desc_lower, archetype_key, domain_id, agents,
        )

        # Generate config
        config = self._generate_config(desc_lower, archetype_key)

        return DomainBlueprint(
            domain_name=name,
            domain_id=domain_id,
            description=domain_desc,
            source_description=description,
            agents=agents,
            skills=skills,
            workflows=workflows,
            config=config,
            creation_mode=creation_mode,
        )

    def _detect_archetype(self, description_lower: str) -> str:
        """Detect the best matching archetype from the description keywords."""
        scores: dict[str, int] = {}
        for keyword, archetype in self._KEYWORD_MAP.items():
            if keyword in description_lower:
                scores[archetype] = scores.get(archetype, 0) + 1
        if scores:
            return str(max(scores, key=lambda k: scores[k]))
        return "research"  # Default fallback

    def _generate_name(
        self, description: str, archetype_key: str,
    ) -> str:
        """Generate a human-readable domain name from the description."""
        # Try to extract a meaningful noun phrase from the description
        # Simple heuristic: take key words and capitalise
        archetype_names = {
            "legal": "Legal Operations",
            "research": "Research",
            "engineering": "Engineering",
            "finance": "Financial Operations",
            "healthcare": "Healthcare",
        }

        # Look for quoted or capitalized phrases
        quoted = re.findall(r'"([^"]+)"', description)
        if quoted:
            return str(quoted[0])

        # Look for "X Domain" or "X domain" pattern
        domain_match = re.search(
            r"(\w+(?:\s+\w+)?)\s+[Dd]omain", description,
        )
        if domain_match:
            return f"{domain_match.group(1).strip()} Domain"

        # Use archetype name + "Domain"
        base = archetype_names.get(archetype_key, "Custom")
        return f"{base} Domain"

    def _generate_id(self, name: str) -> str:
        """Generate a slug-style domain ID from the name."""
        slug = name.lower().strip()
        slug = re.sub(r"[^a-z0-9]+", "-", slug)
        slug = re.sub(r"-+", "-", slug)
        slug = slug.strip("-")
        return slug or "unnamed-domain"

    def _generate_description(
        self, description: str, archetype_key: str,
    ) -> str:
        """Generate a formal domain description."""
        archetype_descs: dict[str, str] = {
            "legal": "Legal operations domain for contract analysis, compliance verification, and regulatory research.",
            "research": "Research domain for systematic investigation, data analysis, and knowledge publication.",
            "engineering": "Engineering domain for software development, deployment, and system reliability.",
            "finance": "Financial operations domain for analysis, risk management, and market research.",
            "healthcare": "Healthcare domain for patient care coordination, diagnosis support, and medical research.",
        }
        base = archetype_descs.get(archetype_key, "Custom domain for specialised task execution.")
        return f"{base} Source: {description}"

    def _generate_agents(
        self,
        description_lower: str,
        archetype_key: str,
        domain_id: str,
    ) -> list[AgentBlueprint]:
        """Generate agent blueprints based on archetype and description."""
        archetype = self._ARCHETYPES.get(archetype_key)
        if archetype is None:
            return self._default_agents(domain_id)

        agent_defs = archetype["agents"]
        if not isinstance(agent_defs, list):
            return self._default_agents(domain_id)

        agents: list[AgentBlueprint] = []
        for i, agent_def in enumerate(agent_defs):
            if not isinstance(agent_def, tuple) or len(agent_def) < 3:
                continue
            role, name, goal = agent_def[0], agent_def[1], agent_def[2]
            agent_id = f"{domain_id}-{role.value}-{i + 1}"
            agents.append(AgentBlueprint(
                agent_id=agent_id,
                name=name,
                role=role,
                goal=str(goal),
                backstory=f"{name} is a {role.value} agent in the {domain_id} domain. {goal}",
                capabilities=[role.value],
            ))

        return agents or self._default_agents(domain_id)

