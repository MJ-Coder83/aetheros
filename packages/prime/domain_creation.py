class BlueprintValidator:
    """Validates domain blueprints before they can be submitted as proposals.

    Checks for:
    - Completeness (minimum agents, skills, workflows)
    - Safety (no conflicting or dangerous configurations)
    - Uniqueness (no duplicate domain names or IDs)
    - Naming conventions (valid slugs, non-empty names)
    """

    MIN_AGENTS = 2
    MIN_SKILLS = 1
    MIN_WORKFLOWS = 1
    MAX_AGENTS = 20
    MAX_SKILLS = 30
    MAX_WORKFLOWS = 10

    def validate(
        self,
        blueprint: DomainBlueprint,
        existing_domains: list[DomainDescriptor],
    ) -> tuple[list[str], list[str]]:
        """Validate a domain blueprint.

        Args:
            blueprint: The blueprint to validate.
            existing_domains: Currently registered domains (for duplicate checks).

        Returns:
            A tuple of (errors, warnings). Errors prevent submission;
            warnings are informational.
        """
        errors: list[str] = []
        warnings: list[str] = []

        # -- Completeness checks --
        if not blueprint.domain_name.strip():
            errors.append("Domain name cannot be empty")

        if not blueprint.domain_id.strip():
            errors.append("Domain ID cannot be empty")

        if not blueprint.description.strip():
            warnings.append("Domain description is empty — consider adding a description")

        if len(blueprint.agents) < self.MIN_AGENTS:
            errors.append(
                f"Domain must have at least {self.MIN_AGENTS} agent(s), "
                f"got {len(blueprint.agents)}"
            )

        if len(blueprint.agents) > self.MAX_AGENTS:
            warnings.append(
                f"Domain has {len(blueprint.agents)} agents — consider reducing to "
                f"{self.MAX_AGENTS} or fewer for manageability"
            )

        if len(blueprint.skills) < self.MIN_SKILLS:
            errors.append(
                f"Domain must have at least {self.MIN_SKILLS} skill(s), "
                f"got {len(blueprint.skills)}"
            )

        if len(blueprint.workflows) < self.MIN_WORKFLOWS:
            errors.append(
                f"Domain must have at least {self.MIN_WORKFLOWS} workflow(s), "
                f"got {len(blueprint.workflows)}"
            )

        # -- Uniqueness checks --
        existing_names = {d.name.lower() for d in existing_domains}
        existing_ids = {d.domain_id.lower() for d in existing_domains}

        if blueprint.domain_name.lower() in existing_names:
            errors.append(
                f"Domain name '{blueprint.domain_name}' already exists"
            )

        if blueprint.domain_id.lower() in existing_ids:
            errors.append(
                f"Domain ID '{blueprint.domain_id}' already exists"
            )

        # -- Naming convention checks --
        if not re.match(r"^[a-z0-9][a-z0-9-]*[a-z0-9]$", blueprint.domain_id) and len(blueprint.domain_id) >= 2:
            errors.append(
                f"Domain ID '{blueprint.domain_id}' must be a lowercase "
                f"alphanumeric slug (e.g. 'legal-research')"
            )

        if len(blueprint.domain_name) > 100:
            warnings.append("Domain name is very long — consider shortening")

        # -- Agent checks --
        agent_ids = [a.agent_id for a in blueprint.agents]
        if len(agent_ids) != len(set(agent_ids)):
            errors.append("Duplicate agent IDs found in blueprint")

        for agent in blueprint.agents:
            if not agent.name.strip():
                errors.append(f"Agent {agent.agent_id} has an empty name")
            if not agent.goal.strip():
                warnings.append(f"Agent '{agent.name}' has no goal defined")

        # -- Skill checks --
        skill_ids = [s.skill_id for s in blueprint.skills]
        if len(skill_ids) != len(set(skill_ids)):
            errors.append("Duplicate skill IDs found in blueprint")

        # -- Workflow checks --
        for wf in blueprint.workflows:
            # Check that workflow agents exist in the blueprint
            missing = set(wf.agent_ids) - set(agent_ids)
            if missing:
                warnings.append(
                    f"Workflow '{wf.name}' references unknown agent(s): {missing}"
                )

        # -- Config checks --
        if blueprint.config.max_agents < len(blueprint.agents):
            warnings.append(
                f"Config max_agents ({blueprint.config.max_agents}) is less than "
                f"the number of agents in the blueprint ({len(blueprint.agents)})"
            )

        # -- Safety checks --
        if "hack" in blueprint.domain_id or "exploit" in blueprint.domain_id:
            errors.append("Domain ID contains potentially unsafe terms")

        return errors, warnings


