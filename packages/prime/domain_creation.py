class CreationMode(StrEnum):
    """How the domain was created."""

    AUTOMATIC = "automatic"  # Fully generated by Prime
    HUMAN_GUIDED = "human_guided"  # Human provided the description
    HYBRID = "hybrid"  # Human described, Prime refined


class DomainStatus(StrEnum):
    """Lifecycle status of a domain."""

    DRAFT = "draft"  # Blueprint generated, not yet proposed
    PROPOSED = "proposed"  # Proposal submitted, awaiting approval
    ACTIVE = "active"  # Approved and registered
    DEPRECATED = "deprecated"  # Domain no longer in use
    ARCHIVED = "archived"  # Permanently archived


class AgentRole(StrEnum):
    """Roles that an agent can play within a domain."""

    COORDINATOR = "coordinator"  # Orchestrates other agents
    ANALYST = "analyst"  # Analyses data and produces insights
    EXECUTOR = "executor"  # Carries out actions and tasks
    REVIEWER = "reviewer"  # Reviews outputs for quality and compliance
    RESEARCHER = "researcher"  # Gathers information and knowledge
    SPECIALIST = "specialist"  # Deep expertise in a narrow area
    MONITOR = "monitor"  # Watches system health and metrics
    COMMUNICATOR = "communicator"  # Interfaces with humans or other domains


class WorkflowType(StrEnum):
    """Types of starter workflows for a domain."""

    SEQUENTIAL = "sequential"  # Step-by-step pipeline
    PARALLEL = "parallel"  # Concurrent execution
    ITERATIVE = "iterative"  # Feedback loop
    PIPELINE = "pipeline"  # Data processing chain
    DEBATE = "debate"  # Multi-agent discussion
    REVIEW = "review"  # Draft-review-approve cycle


