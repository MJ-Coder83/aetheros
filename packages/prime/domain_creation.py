"""One-Click Domain Creation — Instant specialised domains from natural language.

This module enables users (and Prime) to create entirely new specialised domains
with pre-configured agents, skills, workflows, and settings — from a simple
natural language description.

Core flow:

1. **generate_domain_blueprint()** — Parse a natural language description and
   auto-generate a complete domain blueprint with:
   - Domain name, ID, and description
   - 4-8 specialised agents with roles, goals, and backstories
   - Relevant skills (new or reused from existing registry)
   - 2-3 starter crews/workflows
   - Domain-specific configuration and constraints

2. **create_domain_from_description()** — Orchestrate the full creation pipeline:
   blueprint generation -> validation -> Proposal submission -> (await approval)
   -> domain registration.

3. **register_domain()** — Save the new domain to the DomainRegistry after
   proposal approval.

Safety guarantees:

- All new domains go through the Proposal approval workflow
- Duplicate domain names are prevented
- Generated content is validated for completeness and safety
- Both fully-automatic and human-guided creation modes are supported
- All creation events are logged to the Tape

Architecture::

    DomainCreationEngine
    +-- create_domain_from_description()  -- Full pipeline
    +-- generate_domain_blueprint()       -- NL -> DomainBlueprint
    +-- register_domain()                 -- Blueprint -> DomainRegistry
    +-- validate_blueprint()              -- Safety and completeness checks
    +-- list_domains()                    -- List all available domains
    +-- get_domain()                      -- Retrieve a single domain
    +-- get_blueprint()                   -- Retrieve a stored blueprint

Usage::

    engine = DomainCreationEngine(
        tape_service=tape_svc,
        introspector=introspector,
        proposal_engine=proposal_engine,
    )

    # Generate a blueprint from a description
    blueprint = await engine.generate_domain_blueprint(
        description="Create a Legal Research Domain for contract analysis "
                    "and compliance checking",
    )

    # Full creation (generates blueprint, validates, creates proposal)
    result = await engine.create_domain_from_description(
        description="Create a Legal Research Domain for contract analysis "
                    "and compliance checking",
        created_by="human-001",
    )
    # result.proposal.status == "pending_approval"

    # After human approval of the proposal, register the domain
    domain = await engine.register_domain(blueprint.id)
"""

