class DomainCreationEngine:
    """One-Click Domain Creation engine for InkosAI.

    Generates complete domain blueprints from natural language descriptions,
    validates them for safety and completeness, and submits them through
    the Proposal approval workflow for human review.

    All operations are logged to the Tape for full auditability.

    Usage::

        engine = DomainCreationEngine(
            tape_service=tape_svc,
            introspector=introspector,
            proposal_engine=proposal_engine,
        )

        # Generate a blueprint (no side effects)
        blueprint = await engine.generate_domain_blueprint(
            description="Create a Legal Research Domain for contract analysis",
        )

        # Full creation pipeline (generates + validates + proposes)
        result = await engine.create_domain_from_description(
            description="Create a Legal Research Domain for contract analysis",
            created_by="human-001",
        )

        # After approval, register the domain
        domain = await engine.register_domain(blueprint.id)
    """

    def __init__(
        self,
        tape_service: TapeService,
        introspector: PrimeIntrospector | None = None,
        proposal_engine: ProposalEngine | None = None,
        domain_registry: DomainRegistry | None = None,
        blueprint_store: BlueprintStore | None = None,
        generator: BlueprintGenerator | None = None,
        validator: BlueprintValidator | None = None,
        folder_tree_service: FolderTreeService | None = None,
        aethergit: AdvancedAetherGit | None = None,
    ) -> None:
        self._tape = tape_service
        self._introspector = introspector
        self._proposal_engine = proposal_engine
        self._domain_registry = domain_registry or DomainRegistry()
        self._store = blueprint_store or BlueprintStore()
        self._generator = generator or BlueprintGenerator()
        self._validator = validator or BlueprintValidator()
        self._folder_tree_service = folder_tree_service
        self._aethergit = aethergit

    # ------------------------------------------------------------------
    # generate_domain_blueprint
    # ------------------------------------------------------------------

    async def generate_domain_blueprint(
        self,
        description: str,
        domain_name: str | None = None,
        creation_mode: CreationMode = CreationMode.HUMAN_GUIDED,
        created_by: str = "prime",
    ) -> DomainBlueprint:
        """Generate a domain blueprint from a natural language description.

        This is a read-only operation — it does not create any proposals
        or register any domains. It produces a blueprint that can be
        reviewed, modified, and then submitted.

        Args:
            description: Natural language description of the desired domain.
            domain_name: Optional explicit name; auto-generated if not provided.
            creation_mode: How the domain is being created.
            created_by: Who initiated the generation.

        Returns:
            A DomainBlueprint ready for validation and submission.

        Raises:
            ValueError: if the description is empty.
        """
        if not description.strip():
            raise ValueError("Domain description cannot be empty")

        # Get existing skills for potential reuse
        existing_skills: list[SkillDescriptor] = []
        if self._introspector is not None:
            existing_skills = self._introspector._skills.list_skills()

        # Generate the blueprint
        blueprint = self._generator.generate(
            description=description,
            domain_name=domain_name,
            creation_mode=creation_mode,
            existing_skills=existing_skills,
        )
        blueprint.created_by = created_by

        # Validate immediately so the user can see any issues
        existing_domains = self._domain_registry.list_domains()
        errors, warnings = self._validator.validate(blueprint, existing_domains)
        blueprint.validation_errors = errors
        blueprint.validation_warnings = warnings

        # Store the blueprint
        self._store.add(blueprint)

        # Log to Tape
        await self._tape.log_event(
            event_type="domain.blueprint_generated",
            payload={
                "blueprint_id": str(blueprint.id),
                "domain_name": blueprint.domain_name,
                "domain_id": blueprint.domain_id,
                "agent_count": len(blueprint.agents),
                "skill_count": len(blueprint.skills),
                "workflow_count": len(blueprint.workflows),
                "validation_errors": len(errors),
                "validation_warnings": len(warnings),
                "created_by": created_by,
            },
            agent_id="domain-creation-engine",
        )

        return blueprint

    # ------------------------------------------------------------------
    # create_domain_from_description
    # ------------------------------------------------------------------

    async def create_domain_from_description(
        self,
        description: str,
        domain_name: str | None = None,
        creation_mode: CreationMode = CreationMode.HUMAN_GUIDED,
        created_by: str = "prime",
    ) -> DomainCreationResult:
        """Full domain creation pipeline: generate, validate, propose.

        This generates a blueprint, validates it, and submits a Proposal
        for approval. The domain is NOT registered until the proposal is
        approved and ``register_domain()`` is called.

        Args:
            description: Natural language description of the desired domain.
            domain_name: Optional explicit name.
            creation_mode: How the domain is being created.
            created_by: Who initiated the creation.

        Returns:
            A DomainCreationResult with the blueprint and proposal info.

        Raises:
            BlueprintValidationError: if the blueprint has validation errors.
            ValueError: if the description is empty.
        """
        # Step 1: Generate blueprint
        blueprint = await self.generate_domain_blueprint(
            description=description,
            domain_name=domain_name,
            creation_mode=creation_mode,
            created_by=created_by,
        )

        # Step 2: Check for validation errors
        if blueprint.validation_errors:
            raise BlueprintValidationError(
                f"Blueprint has {len(blueprint.validation_errors)} validation error(s): "
                + "; ".join(blueprint.validation_errors)
            )

        # Step 3: Submit proposal
        proposal_id: UUID | None = None
        if self._proposal_engine is not None:
            # Determine risk level based on config
            risk = RiskLevel.LOW
            if blueprint.config.priority_level in ("high", "critical"):
                risk = RiskLevel.MEDIUM
            if blueprint.config.requires_human_approval:
                risk = RiskLevel.MEDIUM
            if blueprint.config.priority_level == "critical":
                risk = RiskLevel.HIGH

            # Build implementation steps
            impl_steps = [
                f"Register domain '{blueprint.domain_name}' with ID '{blueprint.domain_id}'",
                f"Create {len(blueprint.agents)} agent(s): "
                + ", ".join(a.name for a in blueprint.agents),
                f"Set up {len(blueprint.skills)} skill(s): "
                + ", ".join(s.name for s in blueprint.skills),
                f"Configure {len(blueprint.workflows)} workflow(s): "
                + ", ".join(w.name for w in blueprint.workflows),
                "Apply domain configuration and constraints",
            ]

            proposal = await self._proposal_engine.propose(
                title=f"Create Domain: {blueprint.domain_name}",
                modification_type=ModificationType.DOMAIN_CREATION,
                description=f"Create the '{blueprint.domain_name}' domain: {blueprint.description}",
                reasoning=f"Domain creation requested by {created_by} based on description: {blueprint.source_description}",
                expected_impact=f"Adds a new '{blueprint.domain_name}' domain with "
                f"{len(blueprint.agents)} agents, {len(blueprint.skills)} skills, "
                f"and {len(blueprint.workflows)} workflows.",
                risk_level=risk,
                implementation_steps=impl_steps,
                proposed_by=created_by,
            )
            proposal_id = proposal.id
            blueprint.proposal_id = proposal.id
            blueprint.status = DomainStatus.PROPOSED
            self._store.update(blueprint)

        # Step 4: Log creation event
        await self._tape.log_event(
            event_type="domain.creation_requested",
            payload={
                "blueprint_id": str(blueprint.id),
                "domain_name": blueprint.domain_name,
                "domain_id": blueprint.domain_id,
                "proposal_id": str(proposal_id) if proposal_id else None,
                "created_by": created_by,
                "creation_mode": creation_mode.value,
            },
            agent_id="domain-creation-engine",
        )

        return DomainCreationResult(
            blueprint=blueprint,
            proposal_id=proposal_id,
            registered=False,
            message="Domain blueprint generated and proposal submitted. "
            "Awaiting human approval before registration.",
        )

    # ------------------------------------------------------------------
    # register_domain
    # ------------------------------------------------------------------

    async def register_domain(
        self,
        blueprint_id: UUID,
        reviewer: str | None = None,
    ) -> DomainDescriptor:
        """Register a domain after its proposal has been approved.

        This is the final step — it creates the domain in the registry
        and registers all agents and skills.

        Args:
            blueprint_id: The UUID of the blueprint to register.
            reviewer: The human who approved the proposal.

        Returns:
            The registered DomainDescriptor.

        Raises:
            BlueprintNotFoundError: if the blueprint does not exist.
            DomainNotApprovedError: if the associated proposal is not approved.
            DuplicateDomainError: if the domain already exists.
        """
        blueprint = self._store.get(blueprint_id)
        if blueprint is None:
            raise BlueprintNotFoundError(
                f"Blueprint {blueprint_id} not found"
            )

        # Check proposal status if a proposal was created
        if blueprint.proposal_id is not None and self._proposal_engine is not None:
            proposal = self._proposal_engine._store.get(blueprint.proposal_id)
            if proposal is None or proposal.status not in (
                ProposalStatus.APPROVED, ProposalStatus.IMPLEMENTED,
            ):
                raise DomainNotApprovedError(
                    f"Domain '{blueprint.domain_name}' cannot be registered — "
                    f"proposal {blueprint.proposal_id} is not approved "
                    f"(status: {proposal.status.value if proposal else 'unknown'})"
                )

        # Check for duplicate domain
        existing = self._domain_registry.get_domain(blueprint.domain_id)
        if existing is not None:
            raise DuplicateDomainError(
                f"Domain with ID '{blueprint.domain_id}' already exists"
            )

        # Register the domain
        domain = DomainDescriptor(
            domain_id=blueprint.domain_id,
            name=blueprint.domain_name,
            description=blueprint.description,
            agent_count=len(blueprint.agents),
        )
        self._domain_registry.register(domain)

        # Generate the folder tree (canonical source of truth)
        # Folder tree is an enhancement — don't fail registration on error.
        tree = None
        if self._folder_tree_service is not None:
            with contextlib.suppress(Exception):
                tree = await self._folder_tree_service.create_tree(
                    domain_id=blueprint.domain_id,
                    domain_name=blueprint.domain_name,
                    description=blueprint.description,
                    agents=blueprint.agents,
                    skills=blueprint.skills,
                    workflows=blueprint.workflows,
                    config=blueprint.config,
                )

        # Folder Thinking Mode — Prime validates the generated tree structure
        if tree is not None and self._introspector is not None:
            with contextlib.suppress(Exception):
                await self._introspector.folder_navigate(blueprint.domain_id, "")

        # Commit the folder tree to AetherGit for version control
        if tree is not None and self._aethergit is not None:
            with contextlib.suppress(Exception):
                commit = AetherCommit(
                    author="domain-creation-engine",
                    message=f"Create domain: {blueprint.domain_name}",
                    commit_type="domain_creation",
                    scope=blueprint.domain_id,
                    performance_metrics={
                        "agent_count": len(blueprint.agents),
                        "skill_count": len(blueprint.skills),
                        "workflow_count": len(blueprint.workflows),
                        "node_count": len(tree.nodes),
                    },
                    confidence_score=1.0,
                    proposed_by=reviewer or blueprint.created_by,
                    evolution_approved=True,
                )
                self._aethergit.add_commit(commit, branch=f"domain/{blueprint.domain_id}")

        # Register agents in the introspector's agent registry
        if self._introspector is not None:
            for agent_bp in blueprint.agents:
                self._introspector._agents.register(AgentDescriptor(
                    agent_id=agent_bp.agent_id,
                    name=agent_bp.name,
                    capabilities=agent_bp.capabilities,
                    status="idle",
                ))

        # Register new skills in the introspector's skill registry
        if self._introspector is not None:
            for skill_bp in blueprint.skills:
                if not skill_bp.is_reused:
                    self._introspector._skills.register(SkillDescriptor(
                        skill_id=skill_bp.skill_id,
                        name=skill_bp.name,
                        version=skill_bp.version,
                        description=skill_bp.description,
                    ))

        # Update blueprint status
        blueprint.status = DomainStatus.ACTIVE
        with contextlib.suppress(BlueprintNotFoundError):
            self._store.update(blueprint)

        # Mark proposal as implemented
        if blueprint.proposal_id is not None and self._proposal_engine is not None:
            with contextlib.suppress(Exception):
                await self._proposal_engine.mark_implemented(blueprint.proposal_id)

        # Log to Tape
        await self._tape.log_event(
            event_type="domain.registered",
            payload={
                "blueprint_id": str(blueprint.id),
                "domain_id": blueprint.domain_id,
                "domain_name": blueprint.domain_name,
                "agent_count": len(blueprint.agents),
                "skill_count": len(blueprint.skills),
                "workflow_count": len(blueprint.workflows),
                "reviewer": reviewer,
            },
            agent_id="domain-creation-engine",
        )

        return domain

    # ------------------------------------------------------------------
    # validate_blueprint
    # ------------------------------------------------------------------

    def validate_blueprint(
        self,
        blueprint: DomainBlueprint,
    ) -> tuple[list[str], list[str]]:
        """Validate a blueprint without storing or submitting it.

        Args:
            blueprint: The blueprint to validate.

        Returns:
            A tuple of (errors, warnings).
        """
        existing_domains = self._domain_registry.list_domains()
        return self._validator.validate(blueprint, existing_domains)

    # ------------------------------------------------------------------
    # Query methods
    # ------------------------------------------------------------------

    async def list_domains(self) -> list[DomainDescriptor]:
        """List all registered domains."""
        return self._domain_registry.list_domains()

    async def get_domain(self, domain_id: str) -> DomainDescriptor | None:
        """Retrieve a single domain by ID."""
        return self._domain_registry.get_domain(domain_id)

    async def get_blueprint(self, blueprint_id: UUID) -> DomainBlueprint:
        """Retrieve a stored blueprint by ID.

        Raises:
            BlueprintNotFoundError: if the blueprint does not exist.
        """
        bp = self._store.get(blueprint_id)
        if bp is None:
            raise BlueprintNotFoundError(
                f"Blueprint {blueprint_id} not found"
            )
        return bp

    async def list_blueprints(self) -> list[DomainBlueprint]:
        """List all stored blueprints."""
        return self._store.list_all()

    async def delete_blueprint(self, blueprint_id: UUID) -> None:
        """Delete a stored blueprint.

        Raises:
            BlueprintNotFoundError: if the blueprint does not exist.
        """
        bp = self._store.get(blueprint_id)
        if bp is None:
            raise BlueprintNotFoundError(
                f"Blueprint {blueprint_id} not found"
            )
        if bp.status == DomainStatus.ACTIVE:
            raise DomainCreationError(
                f"Cannot delete blueprint for active domain '{bp.domain_name}'"
            )
        self._store.remove(blueprint_id)
