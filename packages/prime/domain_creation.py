    def _generate_skills(
        self,
        description_lower: str,
        archetype_key: str,
        domain_id: str,
        existing_skills: list[SkillDescriptor],
    ) -> list[SkillBlueprint]:
        """Generate skill blueprints, reusing existing skills where possible."""
        archetype = self._ARCHETYPES.get(archetype_key)
        if archetype is None:
            return self._default_skills(domain_id)

        skill_names = archetype["skills"]
        if not isinstance(skill_names, list):
            return self._default_skills(domain_id)

        existing_by_name: dict[str, SkillDescriptor] = {
            s.name.lower(): s for s in existing_skills
        }

        skills: list[SkillBlueprint] = []
        for skill_name in skill_names:
            existing = existing_by_name.get(skill_name.lower())
            if existing is not None:
                skills.append(SkillBlueprint(
                    skill_id=existing.skill_id,
                    name=existing.name,
                    description=existing.description,
                    version=existing.version,
                    is_reused=True,
                    source_domain=None,
                ))
            else:
                skill_id = f"{domain_id}-{skill_name.replace('_', '-')}"
                skills.append(SkillBlueprint(
                    skill_id=skill_id,
                    name=skill_name.replace("_", " ").title(),
                    description=f"{skill_name.replace('_', ' ')} capability for the {domain_id} domain",
                    is_reused=False,
                ))

        return skills or self._default_skills(domain_id)

    def _default_skills(self, domain_id: str) -> list[SkillBlueprint]:
        """Generate default skills when no archetype matches."""
        return [
            SkillBlueprint(
                skill_id=f"{domain_id}-task-management",
                name="Task Management",
                description="Core task management capability for the domain",
            ),
            SkillBlueprint(
                skill_id=f"{domain_id}-data-analysis",
                name="Data Analysis",
                description="Data analysis and reporting capability",
            ),
            SkillBlueprint(
                skill_id=f"{domain_id}-communication",
                name="Communication",
                description="Inter-agent and human communication capability",
            ),
        ]

    def _generate_workflows(
        self,
        description_lower: str,
        archetype_key: str,
        domain_id: str,
        agents: list[AgentBlueprint],
    ) -> list[WorkflowBlueprint]:
        """Generate workflow blueprints based on archetype and agents."""
        archetype = self._ARCHETYPES.get(archetype_key)
        workflow_defs: list[tuple[WorkflowType, str, str]] = []

        if archetype is not None:
            wf_list = archetype.get("workflows", [])
            if isinstance(wf_list, list):
                for wf in wf_list:
                    if isinstance(wf, tuple) and len(wf) >= 3:
                        workflow_defs.append(wf)

        if not workflow_defs:
            workflow_defs = [
                (WorkflowType.SEQUENTIAL, "Main Pipeline", "Gather -> Process -> Review -> Deliver"),
            ]

        agent_ids = [a.agent_id for a in agents]

        workflows: list[WorkflowBlueprint] = []
        for i, (wf_type, wf_name, wf_desc) in enumerate(workflow_defs):
            steps = [s.strip() for s in wf_desc.split("->")]
            workflows.append(WorkflowBlueprint(
                workflow_id=f"{domain_id}-workflow-{i + 1}",
                name=wf_name,
                workflow_type=wf_type,
                description=wf_desc,
                agent_ids=agent_ids[:min(len(agent_ids), 4)],
                steps=steps,
            ))

        return workflows

    def _generate_config(
        self,
        description_lower: str,
        archetype_key: str,
    ) -> DomainConfig:
        """Generate domain configuration based on archetype."""
        # High-risk domains require human approval
        high_risk_archetypes = {"legal", "healthcare", "finance"}
        requires_approval = archetype_key in high_risk_archetypes

        priority = "normal"
        if archetype_key in ("healthcare",):
            priority = "critical"
        elif archetype_key in ("finance", "legal"):
            priority = "high"

        return DomainConfig(
            max_agents=10,
            max_concurrent_tasks=5,
            requires_human_approval=requires_approval,
            data_retention_days=90,
            priority_level=priority,
        )


